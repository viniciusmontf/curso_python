'''
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas

'''

# Aspas simples
print('Vinícius Monteiro')
print('Vinícius "Monteiro"') # Aspas dentro do print

# Aspas duplas
print("Vinícius Monteiro")
print("Vinícius 'Monteiro'") # Aspas dentro do print

# Escape
print("Vinícius \"Monteiro\"")

# r
print(r"Vinícius \"Monteiro\"")