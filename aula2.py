# o print por padrão separa os argumentos com espaço e quebra linha no final
print(12, 34, 1001, sep='-') # o parametro "sep" permite alterar o separador

print(56, 78, sep='+', end='\n') # o parametro "end" muda o comportamento do final do print (quebra de linha por padrao)
print(9, 10, sep='-', end='##')